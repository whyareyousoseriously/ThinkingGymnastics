public class Father {
	private int i;
	private String s;
	private char c;
	public Father() {
		this.i = 0;
		this.s = "str";
		this.c = 'c';
	}
	public Father(int i, String s, char c) {
		super();
		this.i = i;
		this.s = s;
		this.c = c;
	}
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public char getC() {
		return c;
	}
	public void setC(char c) {
		this.c = c;
	}
	
	
}
